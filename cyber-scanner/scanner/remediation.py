def _build_upgrade_script(target: str, date: str, vulns: list[dict[str, Any]]) -> str:
    pip_lines: list[str] = []
    npm_lines: list[str] = []

    seen: set[str] = set()
    for vuln in vulns:
        pkg = vuln.get("package", "")
        eco = vuln.get("ecosystem", "")
        if not pkg or pkg in seen:
            continue
        seen.add(pkg)
        cve_str = ", ".join(vuln.get("cve_ids", []))
        comment = f"# {cve_str}" if cve_str else ""
        if eco == "PyPI":
            pip_lines.append(f"pip install --upgrade {pkg}  {comment}".rstrip())
        elif eco == "npm":
            npm_lines.append(f"npm update {pkg}  {comment}".rstrip())

    pip_block = "\n".join(pip_lines) if pip_lines else "# No PyPI vulnerabilities found"
    npm_block = "\n".join(npm_lines) if npm_lines else "# No npm vulnerabilities found"
    return _UPGRADE_TEMPLATE.format(date=date, target=target, pip_commands=pip_block, npm_commands=npm_block)


