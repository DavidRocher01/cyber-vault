def _build_email_security(target: str, date: str, email_result: dict[str, Any]) -> str:
    domain = _extract_domain(target)
    issues: list[str] = []

    def _missing(field: str) -> bool:
        val = email_result.get(field)
        if isinstance(val, dict):
            return not val.get("valid") and not val.get("found")
        return not val  # bool False or None → missing

    if _missing("spf"):
        issues.append("SPF record missing or invalid")
    if _missing("dkim"):
        issues.append("DKIM record not found")
    if _missing("dmarc"):
        issues.append("DMARC record missing or invalid")
    comment_lines = "\n".join(f"#    - {i}" for i in issues) if issues else "#    - Email security records not fully configured"
    return _EMAIL_SECURITY_TEMPLATE.format(date=date, target=target, domain=domain, email_issues_comment=comment_lines)


