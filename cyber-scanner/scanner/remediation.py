from urllib.parse import urlparse
from fastapi import HTTPException
from fastapi.responses import RedirectResponse

# Allowlist of trusted redirect domains
ALLOWED_DOMAINS: set[str] = {{
    "{domain}",
    # Add other trusted domains here
}}


def safe_redirect(url: str, status_code: int = 302) -> RedirectResponse:
    """
    Safely redirect to a URL, rejecting external domains not in the allowlist.

    Args:
        url:         The target URL (user-supplied or internal).
        status_code: HTTP status code (302 by default).

    Raises:
        HTTPException 400 if the redirect target is not allowed.
    """
    parsed = urlparse(url)

    # Allow relative URLs (no scheme/netloc)
    if not parsed.scheme and not parsed.netloc:
        return RedirectResponse(url=url, status_code=status_code)

    # Reject external domains not in the allowlist
    if parsed.netloc not in ALLOWED_DOMAINS:
        raise HTTPException(
            status_code=400,
            detail=f"Redirect to '{{parsed.netloc}}' is not allowed.",
        )

    return RedirectResponse(url=url, status_code=status_code)
