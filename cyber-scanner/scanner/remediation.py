_COOKIES_TEMPLATE = '''\
"""
FastAPI Secure Cookie Middleware — auto-generated by Cyber-Scanner v1.0
Generated : {date}
Target    : {target}

ISSUES DETECTED
---------------
{cookie_issues}

FIX
---
This middleware rewrites Set-Cookie headers to add missing security flags.
Add it to your FastAPI app:

    from cookie_security import SecureCookieMiddleware
    app.add_middleware(SecureCookieMiddleware)
"""

from starlette.middleware.base import BaseHTTPMiddleware
from starlette.requests import Request
from starlette.responses import Response


class SecureCookieMiddleware(BaseHTTPMiddleware):
    """Enforce HttpOnly, Secure, and SameSite=Strict on all Set-Cookie headers."""

    async def dispatch(self, request: Request, call_next) -> Response:
        response = await call_next(request)
        new_cookies = []
        for cookie in response.headers.getlist("set-cookie"):
            cookie = self._enforce_flags(cookie)
            new_cookies.append(cookie)
        if new_cookies:
            # Replace all Set-Cookie headers
            headers = list(response.headers.items())
            headers = [(k, v) for k, v in headers if k.lower() != "set-cookie"]
            for c in new_cookies:
                headers.append(("set-cookie", c))
            response.headers.__dict__["_list"] = headers
        return response

    @staticmethod
    def _enforce_flags(cookie: str) -> str:
        parts = [part.strip() for part in cookie.split(";")]
        flags = {{part.lower().split("=")[0]: part for part in parts[1:]}}

        if "httponly" not in flags:
            parts.append("HttpOnly")
        if "secure" not in flags:
            parts.append("Secure")
        if "samesite" not in flags:
            parts.append("SameSite=Strict")

        return "; ".join(parts)
'''

